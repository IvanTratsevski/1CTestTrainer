function createQuiz(section) {
    const quizContainer = document.getElementById('quiz');
    quizContainer.innerHTML = ''; // Очищаем контейнер перед новым тестом
    let questions;

    if (section === "Random") {
        questions = getRandomQuestions();
    } else {
        questions = quizzes[section];
    }

    questions.forEach((q, index) => {
        const questionBlock = document.createElement('div');
        questionBlock.classList.add('question');

        // Добавляем вопрос и изображение, если оно указано
        questionBlock.innerHTML = `<p>${index + 1}. ${q.question}</p>`;
        if (q.image) {
            const img = document.createElement('img');
            img.src = q.image;
            img.alt = "Изображение вопроса";
            img.style.maxWidth = "100%"; // Устанавливаем максимальную ширину для адаптивности
            img.style.borderRadius = "5px"; // Закругляем углы изображения
            questionBlock.appendChild(img);
        }
        
        q.options.forEach(option => {
            const optionBlock = document.createElement('div');
            optionBlock.classList.add('options');
            optionBlock.innerHTML = `<label><input type="radio" name="question${index}" value="${option}" onchange="checkSingleAnswer(${index}, '${q.answer}', this)"> ${option}</label>`;
            questionBlock.appendChild(optionBlock);
        });

        const feedbackBlock = document.createElement('div');
        feedbackBlock.classList.add('feedback');
        feedbackBlock.style.display = 'none'; // Скрываем блок с ответом
        questionBlock.appendChild(feedbackBlock);
        
        quizContainer.appendChild(questionBlock);
    });
}

function getRandomQuestions() {
    const randomQuestions = [];
    for (const section in quizzes) {
        const questions = quizzes[section];
        const randomIndex = Math.floor(Math.random() * questions.length);
        randomQuestions.push(questions[randomIndex]);
    }
    return randomQuestions;
}

function checkSingleAnswer(index, correctAnswer, selectedOption) {
    const feedbackBlock = document.querySelector(`.question:nth-child(${index + 1}) .feedback`);
    const questionBlock = document.querySelector(`.question:nth-child(${index + 1})`);

    if (selectedOption.value === correctAnswer) {
        feedbackBlock.innerHTML = `<strong class="correct">Правильно!</strong>`;
        questionBlock.style.backgroundColor = '#d4edda'; // Светло-зелёный для правильного ответа
    } else {
        feedbackBlock.innerHTML = `<strong class="incorrect">Неправильно!</strong><br>Правильный ответ: ${correctAnswer}`;
        questionBlock.style.backgroundColor = '#f8d7da'; // Светло-красный для неправильного ответа
    }

    feedbackBlock.style.display = 'block'; // Показываем блок с ответом
}

// Функция для выбора раздела
function selectSection(section) {
    createQuiz(section);
}

// Функция для создания выпадающего списка
function createSectionSelect() {
    const sectionSelect = document.getElementById('section-select');
    sectionSelect.innerHTML = ''; // Очищаем текущие опции

    // Добавляем опцию "Случайный тест"
    const randomOption = document.createElement('option');
    randomOption.value = "Random";
    randomOption.textContent = "Случайный тест";
    sectionSelect.appendChild(randomOption);

    // Добавляем опции для каждого раздела
    for (const section in quizzes) {
        const option = document.createElement('option');
        option.value = section;
        option.textContent = section;
        sectionSelect.appendChild(option);
    }

    // Установить обработчик события для изменения выбора
    sectionSelect.addEventListener('change', (event) => {
        selectSection(event.target.value);
    });
}

